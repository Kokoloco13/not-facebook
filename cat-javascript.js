function followOliver(){
    var followOliver = document.getElementById("followOliver");
    if (followOliver.classList.contains("following")) {
        followOliver.innerHTML = "Follow";
        followOliver.classList.add("follow-button");
        followOliver.classList.remove("following");
    } else {
        followOliver.innerHTML = "Following";
        followOliver.classList.add("following");
        followOliver.classList.remove("follow-button");
    }
}

